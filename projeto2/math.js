console.log(Math.PI); // Imprime o valor de PI
console.log(Math.E); // Imprime o valor de E

console.log(Math.round(4.7)); // Arredonda o número para o inteiro mais próximo
console.log(Math.ceil(4.2)); // Arredonda o número para cima
console.log(Math.floor(4.8)); // Arredonda o número para baixo

console.log(Math.random()); // Gera um número aleatório entre 0 e 1
console.log(Math.random() * 100); // Gera um número aleatório entre 0 e 100

console.log(Math.max(5, 10, 15)); // Retorna o maior número
console.log(Math.min(5, 10, 15)); // Retorna o menor número

console.log(Math.pow(2, 3)); // Eleva 2 à potência de 3
console.log(Math.sqrt(16)); // Retorna a raiz quadrada de 16
console.log(Math.abs(-5)); // Retorna o valor absoluto de -5

console.log(Math.sin(Math.PI / 2)); // Retorna o seno de PI/2
console.log(Math.cos(0)); // Retorna o cosseno de 0
console.log(Math.tan(Math.PI / 4)); // Retorna a tangente de PI/4

console.log(Math.log(1)); // Retorna o logaritmo natural de 1
console.log(Math.exp(1)); // Retorna o valor de E elevado à potência de 1

